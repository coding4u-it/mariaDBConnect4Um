    private void btnConnectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConnectActionPerformed
        String pp[]=new String[3];int i=0;
        do{
            pp[0]=JOptionPane.showInputDialog("Autenticazione:\n Inserire username");
            //pp[1]=JOptionPane.showInputDialog("Autenticazione:\n Inserire password");
            //pp[2]=JOptionPane.showInputDialog("Inserisci nome Database");
            objDB=new GestDBmyLiteSwing("UsrSardegna",pp[0],pp[0]+"@20");
            i++;
        }
        while (!objDB.ctrlDB().equalsIgnoreCase("ok")&& i<3);
        if(i>=3){
            JOptionPane.showMessageDialog(this, "Inserimento effettuato troppe volte! Arrivederci");
            System.exit(1);
        }
        btnConnect.setEnabled(false);
        lblConn.setBackground(Color.green);
        lblConn.setText("1");
        jtp.setEnabled(true);
        try {
            lstCat.setListData(objDB.getCatalogMetadata());
        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
        lblCat.setEnabled(true);
        lstCat.setEnabled(true);
    }//GEN-LAST:event_btnConnectActionPerformed

