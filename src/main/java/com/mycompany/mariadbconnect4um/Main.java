    private void lstCatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstCatMouseClicked
        objDB.setNomeDB(lstCat.getSelectedValue());
        lblTab.setText("Tabelle Catalogo "+lstCat.getSelectedValue());
        try {
            table=objDB.getTablesMetadata();
        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
        cbxTabelle.removeAllItems();
        cbxTabelle.addItem("Select");
        riga=0;
        model2.setRowCount(0);//cancella tabella
        model2.setRowCount(riga + 1); //nuova riga
        int c=1;
        for(String s:table){
            cbxTabelle.addItem(s);
            model2.setValueAt(s, riga, c-1);   
            if(c%3==0){
                riga++; c=1;
                model2.setRowCount(riga + 1); //nuova riga 
                }
            else
                c++;
        }
        try {
            objDB.closeRs();
            objDB.closeConn();
        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
        cbxTabelle.setEnabled(true);
    }//GEN-LAST:event_lstCatMouseClicked

    private void btnViewTabActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewTabActionPerformed
        model.setRowCount(0);//cancella tabella
        objDB.dirQuery("SELECT * FROM "+cbxTabelle.getSelectedItem());
        btnDownload.setEnabled(true);
        try {
            objDB.tabella(model);
        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
        jtp.setSelectedIndex(1);
    }//GEN-LAST:event_btnViewTabActionPerformed
   
